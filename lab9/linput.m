function [x p q k a b t n f] = linput()
  %
  % * y''(x) - (x + 3)^2 * y'(x) - 1/(x + 3)^2 * y(x) = 3,
  % * x in [0,1]
  % * y(0) - y'(0) = 4/3
  % * y(1) = 3/4
  %
  n = 50;
  x = [0 1];


  p = @(x) -(x + 3)^2;
  q = @(x) -(x + 3)^-2;
  f = @(x) 3;

  t = abs(x(1) - x(2)) / n;
  a = 4/3;
  b = 3/4;
  k = [1 -1 1 0];
end
